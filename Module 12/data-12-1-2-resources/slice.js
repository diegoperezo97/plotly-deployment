var integers = [0, 1, 2, 3, 4, 5];
var slicedIntegers = integers.slice(0, 2);
console.log(slicedIntegers)

var words = ['seal', 'dog', 'scorpion', 'orangutan', 'salamander'];
var slicedWords = words.slice(0, 3)
console.log(slicedWords)

// To slice to the end of an array, you can omit the second argument:
console.log(words.slice(3, ));

